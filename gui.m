% --- Executes on selection change in popupmenu4.
function popupmenu4_Callback(hObject, eventdata, handles)
% hObject    handle to popupmenu4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Img = handles.Img;
idMode = get (hObject,'value');
if idMode ==1
    cla ('reset');
    imshow (Img);
    grid on;
elseif idMode ==2
    axes(handles.axes1);
    cla ('reset');
    x2=rgb2gray(Img);
    imshow(x2),title('Grayscale');
    grid on;
elseif idMode ==3
    axes(handles.axes1);
    cla ('reset');
    level = graythresh (Img);
    level = 0.3451;
    bw = im2bw (Img,level);
    imshow(bw),title('Graythresh');
    grid on;
elseif idMode ==4
    axes(handles.axes1);
    cla ('reset');
    x2=rgb2gray(Img);
    thresh=150;
    y=thresholding(x2,thresh);
    grid on;
    imshow(y),title('Thresholder');
elseif idMode ==5
    axes(handles.axes1);
    cla ('reset');
    x2=rgb2gray(Img);
    thresh=150;
    y=thresholding(x2,thresh);
    f1=[-1 -2 1;0 0 0;1 2 -1];
    z = imfilter(y,f1);
    grid on;
    imshow(z),title('Deteksi Tepi');
else
   return;
end
     


